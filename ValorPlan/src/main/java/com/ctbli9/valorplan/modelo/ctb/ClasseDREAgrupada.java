package com.ctbli9.valorplan.modelo.ctb;

public class ClasseDREAgrupada {
	private int cdClasse;
	private String cdNivelClasse;
	private String[] dsClasse;
	
	public int getCdClasse() {
		return cdClasse;
	}
	public void setCdClasse(int cdClasse) {
		this.cdClasse = cdClasse;
	}
	
	public String getCdNivelClasse() {
		return cdNivelClasse;
	}
	public void setCdNivelClasse(String cdNivelClasse) {
		this.cdNivelClasse = cdNivelClasse;
	}
	public String[] getDsClasse() {
		return dsClasse;
	}
	public void setDsClasse(String[] dsClasse) {
		this.dsClasse = dsClasse;
	}
}
